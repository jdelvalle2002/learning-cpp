#include <iostream>

using namespace std;

int suma (int a, int b)
{
    int s;
    s = a+b;
    return s; 
}
int resta (int c, int d)
{
    int r;
    r = c-d;
    return r; 
}
int main()
{
    int x;
    int y;
    cout << "nro1: ";
    cin >> x;
    cout << "nro2: ";
    cin >> y;
    su = suma (x,y);
    re = resta (x,y);
    cout << "La suma es " << su << ", y la resta es " << re << endl;

    
}
